#!/usr/bin/env node
/**
 * generate-patches-nextjs.mjs
 *
 * Generates one surgical git patch file per file for the Next.js codegen
 * project. Each patch encodes the delta between raw `btst init` output and
 * the desired E2E-ready state.
 *
 * THE PATCH FILES ARE THE SOURCE OF TRUTH.
 * Edit them directly in the IDE, or use the regeneration workflow below.
 *
 * ── Workflow for updating a patch ────────────────────────────────────────────
 *
 *   1. Create a fresh baseline (shadcn + btst init, committed to git):
 *        bash scripts/codegen/cleanup.sh nextjs
 *        bash scripts/codegen/setup-nextjs.sh --baseline-only
 *
 *   2. Edit files in codegen-projects/nextjs/ to the desired state.
 *      You can copy existing patch content and apply it by hand, or make new
 *      changes from scratch.
 *
 *   3. Regenerate patches from the delta:
 *        node scripts/codegen/generate-patches-nextjs.mjs
 *
 *   4. Commit the updated .patch files.
 *
 * ── Single-file quick update ─────────────────────────────────────────────────
 *
 *   After step 1+2, you can also regenerate just one file:
 *        cd codegen-projects/nextjs
 *        git diff app/layout.tsx \
 *          > ../../scripts/codegen/patches/nextjs/app--layout.tsx.patch
 *
 * ── Requirements ─────────────────────────────────────────────────────────────
 *
 *   - codegen-projects/nextjs/ must exist (run --baseline-only first)
 *   - packages/cli must be built (pnpm --filter @btst/codegen build)
 *
 * Usage (from repo root):
 *   node scripts/codegen/generate-patches-nextjs.mjs
 */

import { execSync } from "child_process";
import {
	mkdtempSync,
	writeFileSync,
	mkdirSync,
	readFileSync,
	rmSync,
	existsSync,
	readdirSync,
	unlinkSync,
} from "fs";
import { tmpdir } from "os";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT_DIR = join(__dirname, "../..");
const PATCHES_DIR = join(__dirname, "patches/nextjs");
const CLI_LIB = join(ROOT_DIR, "packages/cli/dist/lib.cjs");
const CODEGEN_DIR = join(ROOT_DIR, "codegen-projects/nextjs");

// ── Guards ────────────────────────────────────────────────────────────────────

if (!existsSync(CLI_LIB)) {
	console.error(
		"ERROR: CLI not built.\n" + "  Run: pnpm --filter @btst/codegen build",
	);
	process.exit(1);
}

if (!existsSync(CODEGEN_DIR)) {
	console.error(
		"ERROR: codegen-projects/nextjs does not exist.\n" +
			"  Run the baseline setup first:\n" +
			"    bash scripts/codegen/cleanup.sh nextjs\n" +
			"    bash scripts/codegen/setup-nextjs.sh --baseline-only\n" +
			"  Then edit files to the desired state and re-run this script.",
	);
	process.exit(1);
}

// ── Load CLI buildScaffoldPlan ────────────────────────────────────────────────

const { buildScaffoldPlan } = await import(CLI_LIB);

// The exact plugin list used by the codegen project (from setup-nextjs.sh)
const PLUGINS = [
	"blog",
	"ai-chat",
	"cms",
	"form-builder",
	"ui-builder",
	"kanban",
	"comments",
	"media",
	"route-docs",
	"open-api",
];

console.log("Rendering CLI baseline...");
const plan = await buildScaffoldPlan({
	framework: "nextjs",
	adapter: "memory",
	plugins: PLUGINS,
	alias: "@/",
	cssFile: "app/globals.css",
});

// ── Set up temp git repo with CLI baseline ────────────────────────────────────

const tmpDir = mkdtempSync(join(tmpdir(), "btst-patch-gen-"));

function exec(cmd) {
	return execSync(cmd, { cwd: tmpDir, stdio: "pipe" }).toString();
}

function writeFile(relPath, content) {
	const fullPath = join(tmpDir, relPath);
	mkdirSync(dirname(fullPath), { recursive: true });
	writeFileSync(fullPath, content, "utf8");
}

exec("git init -b main");
exec('git config user.email "agent@btst.ai"');
exec('git config user.name "BTST Patch Generator"');

// Write CLI-generated files as baseline
for (const file of plan.files) {
	writeFile(file.path, file.content);
}

// app/layout.tsx is generated by `shadcn init --preset nova`, not btst init.
// We get the true baseline from the codegen project's OWN git history, which
// was committed at the --baseline-only step before any edits were made.
// Fall back to a hardcoded constant only when no git history exists.
let shadcnLayoutBaseline;
try {
	shadcnLayoutBaseline = execSync("git show HEAD:app/layout.tsx", {
		cwd: CODEGEN_DIR,
		stdio: "pipe",
	}).toString();
	console.log(
		"Using app/layout.tsx baseline from codegen project git history (--baseline-only commit)",
	);
} catch {
	// No git history — codegen project wasn't set up with --baseline-only.
	// Use a hardcoded Nova preset baseline. This may drift if shadcn changes.
	console.warn(
		"WARNING: codegen project has no git history.\n" +
			"  For accurate patches, use:\n" +
			"    bash scripts/codegen/cleanup.sh nextjs\n" +
			"    bash scripts/codegen/setup-nextjs.sh --baseline-only\n" +
			"  Then edit files and re-run this script.\n" +
			"  Falling back to hardcoded Nova baseline for app/layout.tsx.",
	);
	shadcnLayoutBaseline = `import { Geist, Geist_Mono } from "next/font/google"

import "./globals.css"
import { ThemeProvider } from "@/components/theme-provider"
import { cn } from "@/lib/utils";

const geist = Geist({subsets:['latin'],variable:'--font-sans'})

const fontMono = Geist_Mono({
  subsets: ["latin"],
  variable: "--font-mono",
})

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  return (
    <html
      lang="en"
      suppressHydrationWarning
      className={cn("antialiased", fontMono.variable, "font-sans", geist.variable)}
    >
      <body>
        <ThemeProvider>{children}</ThemeProvider>
      </body>
    </html>
  )
}
`;
}
writeFile("app/layout.tsx", shadcnLayoutBaseline);

exec("git add -A");
exec('git commit -m "baseline: shadcn init + btst init output"');

// ── Files to diff ─────────────────────────────────────────────────────────────
//
// These are the B-category files: things the E2E project needs that btst init
// does NOT generate (or generates differently). The "after" state is read from
// codegen-projects/nextjs/ — whatever is currently on disk there.
//
// When using --baseline-only + this script, codegen-projects/nextjs is in
// baseline state. Edit files there, then run this script to capture the delta.

const FILES = [
	// Modified: CLI generates these, E2E needs additional content
	"app/layout.tsx",
	"lib/stack.ts",
	"lib/stack-client.tsx",
	"app/pages/layout.tsx",

	// New: not generated by CLI at all — pure E2E additions
	"lib/stack-auth.ts",
	"lib/stack-public-chat.ts",
	"lib/cms-schemas.ts",
	"lib/mock-users.ts",
	"lib/adapters-build-check.ts",

	// Custom todo plugin (full implementation)
	"lib/plugins/todo/schema.ts",
	"lib/plugins/todo/types.ts",
	"lib/plugins/todo/api/backend.ts",
	"lib/plugins/todo/api/getters.ts",
	"lib/plugins/todo/client/client.tsx",
	"lib/plugins/todo/client/components.tsx",
	"lib/plugins/todo/client/hooks.tsx",
	"lib/plugins/todo/client/overrides.ts",

	// E2E demo pages
	"app/cms-example/page.tsx",
	"app/directory/page.tsx",
	"app/directory/[id]/page.tsx",
	"app/directory/category/[categoryId]/page.tsx",
	"app/api/example-auth/[[...all]]/route.ts",
	"app/api/public-chat/[[...all]]/route.ts",

	// UI primitives used by the todo plugin
	"components/ui/empty.tsx",
	"components/ui/field.tsx",
	"components/ui/item.tsx",

	// App chrome (also generated by CLI now, but kept here for the E2E
	// project in case the generated versions differ from what tests expect)
	"components/navbar.tsx",
	"components/mode-toggle.tsx",
];

// ── Wipe existing .patch files ────────────────────────────────────────────────

for (const f of readdirSync(PATCHES_DIR).filter((f) => f.endsWith(".patch"))) {
	unlinkSync(join(PATCHES_DIR, f));
}
console.log("Cleared existing .patch files");

// Convert a relative file path to a flat patch filename:
//   "app/layout.tsx"                → "app--layout.tsx.patch"
//   "lib/plugins/todo/schema.ts"    → "lib--plugins--todo--schema.ts.patch"
function toPatchFilename(relPath) {
	return relPath.replace(/\//g, "--") + ".patch";
}

let written = 0;
let skipped = 0;
let missing = 0;

for (const rel of FILES) {
	const srcPath = join(CODEGEN_DIR, rel);

	if (!existsSync(srcPath)) {
		console.warn(`  [missing] ${rel} — not in codegen project, skipping`);
		missing++;
		continue;
	}

	// Escape $ so shell doesn't expand e.g. $id as a variable
	const shellRel = rel.replace(/\$/g, "\\$");

	const content = readFileSync(srcPath, "utf8");
	writeFile(rel, content);
	exec(`git add "${shellRel}"`);

	const diff = exec(`git diff --cached HEAD -- "${shellRel}"`);

	if (!diff.trim()) {
		// File is identical to CLI baseline — no patch needed
		exec(`git restore --staged "${shellRel}"`);
		exec(`git checkout HEAD -- "${shellRel}"`);
		console.log(`  [no change] ${rel}`);
		skipped++;
		continue;
	}

	const patchFile = join(PATCHES_DIR, toPatchFilename(rel));
	writeFileSync(patchFile, diff, "utf8");
	console.log(`  ✓ ${toPatchFilename(rel)}`);

	// Commit so subsequent single-file diffs are clean
	exec(`git commit -m "apply: ${rel}"`);
	written++;
}

// ── Cleanup ───────────────────────────────────────────────────────────────────

rmSync(tmpDir, { recursive: true, force: true });

console.log(
	`\n${written} patches written, ${skipped} identical to baseline (no patch needed)` +
		(missing ? `, ${missing} missing from codegen project` : ""),
);
if (missing > 0) {
	console.log(
		"\nTip: missing files were not found in codegen-projects/nextjs/.\n" +
			"     Use --baseline-only + manual edits to add them, then re-run.",
	);
}
console.log("Output:", PATCHES_DIR);
