#!/usr/bin/env node
/**
 * generate-patches-react-router.mjs
 *
 * Generates one surgical git patch file per file for the React Router codegen
 * project. Each patch encodes the delta between raw `btst init` output and
 * the desired E2E-ready state.
 *
 * THE PATCH FILES ARE THE SOURCE OF TRUTH.
 * Edit them directly in the IDE, or use the regeneration workflow below.
 *
 * ── Workflow for updating a patch ────────────────────────────────────────────
 *
 *   1. Create a fresh baseline (shadcn + btst init, committed to git):
 *        bash scripts/codegen/cleanup.sh react-router
 *        bash scripts/codegen/setup-react-router.sh --baseline-only
 *
 *   2. Edit files in codegen-projects/react-router/ to the desired state.
 *
 *   3. Regenerate patches from the delta:
 *        node scripts/codegen/generate-patches-react-router.mjs
 *
 *   4. Commit the updated .patch files.
 *
 * ── Single-file quick update ─────────────────────────────────────────────────
 *
 *   After step 1+2, you can also regenerate just one file:
 *        cd codegen-projects/react-router
 *        git diff app/root.tsx \
 *          > ../../scripts/codegen/patches/react-router/app--root.tsx.patch
 *
 * ── Requirements ─────────────────────────────────────────────────────────────
 *
 *   - codegen-projects/react-router/ must exist (run --baseline-only first)
 *   - packages/cli must be built (pnpm --filter @btst/codegen build)
 *
 * Usage (from repo root):
 *   node scripts/codegen/generate-patches-react-router.mjs
 */

import { execSync } from "child_process";
import {
	mkdtempSync,
	writeFileSync,
	mkdirSync,
	readFileSync,
	rmSync,
	existsSync,
	readdirSync,
	unlinkSync,
} from "fs";
import { tmpdir } from "os";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT_DIR = join(__dirname, "../..");
const PATCHES_DIR = join(__dirname, "patches/react-router");
const CLI_LIB = join(ROOT_DIR, "packages/cli/dist/lib.cjs");
const CODEGEN_DIR = join(ROOT_DIR, "codegen-projects/react-router");

// ── Guards ────────────────────────────────────────────────────────────────────

if (!existsSync(CLI_LIB)) {
	console.error(
		"ERROR: CLI not built.\n" + "  Run: pnpm --filter @btst/codegen build",
	);
	process.exit(1);
}

if (!existsSync(CODEGEN_DIR)) {
	console.error(
		"ERROR: codegen-projects/react-router does not exist.\n" +
			"  Run the baseline setup first:\n" +
			"    bash scripts/codegen/cleanup.sh react-router\n" +
			"    bash scripts/codegen/setup-react-router.sh --baseline-only\n" +
			"  Then edit files to the desired state and re-run this script.",
	);
	process.exit(1);
}

// ── Load CLI buildScaffoldPlan ────────────────────────────────────────────────

const { buildScaffoldPlan } = await import(CLI_LIB);

// The exact plugin list used by the codegen project (from setup-react-router.sh)
const PLUGINS = [
	"blog",
	"ai-chat",
	"cms",
	"form-builder",
	"ui-builder",
	"kanban",
	"comments",
	"media",
	"route-docs",
	"open-api",
];

console.log("Rendering CLI baseline...");
const plan = await buildScaffoldPlan({
	framework: "react-router",
	adapter: "memory",
	plugins: PLUGINS,
	alias: "~/",
	cssFile: "app/app.css",
});

// ── Set up temp git repo with CLI baseline ────────────────────────────────────

const tmpDir = mkdtempSync(join(tmpdir(), "btst-patch-gen-rr-"));

function exec(cmd) {
	return execSync(cmd, { cwd: tmpDir, stdio: "pipe" }).toString();
}

function writeFile(relPath, content) {
	const fullPath = join(tmpDir, relPath);
	mkdirSync(dirname(fullPath), { recursive: true });
	writeFileSync(fullPath, content, "utf8");
}

exec("git init -b main");
exec('git config user.email "agent@btst.ai"');
exec('git config user.name "BTST Patch Generator"');

// Write CLI-generated files as baseline
for (const file of plan.files) {
	writeFile(file.path, file.content);
}

// app/root.tsx is generated by `shadcn init`, not btst init.
// We get the true baseline from the codegen project's OWN git history, which
// was committed at the --baseline-only step before any edits were made.
// Fall back to a hardcoded constant only when no git history exists.
let shadcnRootBaseline;
try {
	shadcnRootBaseline = execSync("git show HEAD:app/root.tsx", {
		cwd: CODEGEN_DIR,
		stdio: "pipe",
	}).toString();
	console.log(
		"Using app/root.tsx baseline from codegen project git history (--baseline-only commit)",
	);
} catch {
	console.warn(
		"WARNING: codegen project has no git history.\n" +
			"  For accurate patches, use:\n" +
			"    bash scripts/codegen/cleanup.sh react-router\n" +
			"    bash scripts/codegen/setup-react-router.sh --baseline-only\n" +
			"  Then edit files and re-run this script.\n" +
			"  Falling back to empty baseline for app/root.tsx.",
	);
	shadcnRootBaseline = "";
}
writeFile("app/root.tsx", shadcnRootBaseline);

// app/routes.ts is generated by btst init for react-router.
// Get it from the codegen project's git history as well.
let routesBaseline;
try {
	routesBaseline = execSync("git show HEAD:app/routes.ts", {
		cwd: CODEGEN_DIR,
		stdio: "pipe",
	}).toString();
	console.log("Using app/routes.ts baseline from codegen project git history");
} catch {
	console.warn(
		"WARNING: app/routes.ts not found in git history, using empty baseline.",
	);
	routesBaseline = "";
}
writeFile("app/routes.ts", routesBaseline);

exec("git add -A");
exec('git commit -m "baseline: shadcn init + btst init output"');

// ── Files to diff ─────────────────────────────────────────────────────────────
//
// These are the B-category files: things the E2E project needs that btst init
// does NOT generate (or generates differently). The "after" state is read from
// codegen-projects/react-router/ — whatever is currently on disk there.

const FILES = [
	// Modified: CLI/shadcn generates these, E2E needs different/additional content
	"app/root.tsx",
	"app/lib/stack.ts",
	"app/lib/stack-client.tsx",
	"app/routes/pages/_layout.tsx",
	"app/routes.ts",

	// New: not generated by CLI at all — pure E2E additions
	"app/lib/stack-auth.ts",
	"app/lib/stack-public-chat.ts",
	"app/lib/cms-schemas.ts",
	"app/lib/mock-users.ts",
	"app/lib/adapters-build-check.ts",
	// Note: app/lib/query-client.ts is generated by btst init — no patch needed

	// Custom todo plugin (full implementation)
	"app/lib/plugins/todo/schema.ts",
	"app/lib/plugins/todo/types.ts",
	"app/lib/plugins/todo/api/backend.ts",
	"app/lib/plugins/todo/api/getters.ts",
	"app/lib/plugins/todo/client/client.tsx",
	"app/lib/plugins/todo/client/components.tsx",
	"app/lib/plugins/todo/client/hooks.tsx",
	"app/lib/plugins/todo/client/overrides.ts",

	// E2E demo pages
	"app/routes/cms-example.tsx",
	"app/routes/directory/index.tsx",
	"app/routes/directory/resource.$id.tsx",
	"app/routes/directory/category.$categoryId.tsx",
	"app/routes/api/example-auth/$.ts",
	"app/routes/api/public-chat/$.ts",

	// UI primitives used by the todo plugin
	"app/components/ui/empty.tsx",
	"app/components/ui/field.tsx",
	"app/components/ui/item.tsx",
];

// ── Wipe existing .patch files ────────────────────────────────────────────────

mkdirSync(PATCHES_DIR, { recursive: true });
for (const f of readdirSync(PATCHES_DIR).filter((f) => f.endsWith(".patch"))) {
	unlinkSync(join(PATCHES_DIR, f));
}
console.log("Cleared existing .patch files");

// Convert a relative file path to a flat patch filename:
//   "app/root.tsx"                         → "app--root.tsx.patch"
//   "app/lib/plugins/todo/schema.ts"       → "app--lib--plugins--todo--schema.ts.patch"
function toPatchFilename(relPath) {
	return relPath.replace(/\//g, "--") + ".patch";
}

let written = 0;
let skipped = 0;
let missing = 0;

for (const rel of FILES) {
	const srcPath = join(CODEGEN_DIR, rel);

	if (!existsSync(srcPath)) {
		console.warn(`  [missing] ${rel} — not in codegen project, skipping`);
		missing++;
		continue;
	}

	// Escape $ so shell doesn't expand e.g. $id as a variable
	const shellRel = rel.replace(/\$/g, "\\$");

	const content = readFileSync(srcPath, "utf8");
	writeFile(rel, content);
	exec(`git add "${shellRel}"`);

	const diff = exec(`git diff --cached HEAD -- "${shellRel}"`);

	if (!diff.trim()) {
		// File is identical to CLI baseline — no patch needed
		exec(`git restore --staged "${shellRel}"`);
		exec(`git checkout HEAD -- "${shellRel}"`);
		console.log(`  [no change] ${rel}`);
		skipped++;
		continue;
	}

	const patchFile = join(PATCHES_DIR, toPatchFilename(rel));
	writeFileSync(patchFile, diff, "utf8");
	console.log(`  ✓ ${toPatchFilename(rel)}`);

	// Commit so subsequent single-file diffs are clean
	exec(`git commit -m "apply: ${rel}"`);
	written++;
}

// ── Cleanup ───────────────────────────────────────────────────────────────────

rmSync(tmpDir, { recursive: true, force: true });

console.log(
	`\n${written} patches written, ${skipped} identical to baseline (no patch needed)` +
		(missing ? `, ${missing} missing from codegen project` : ""),
);
if (missing > 0) {
	console.log(
		"\nTip: missing files were not found in codegen-projects/react-router/.\n" +
			"     Use --baseline-only + manual edits to add them, then re-run.",
	);
}
console.log("Output:", PATCHES_DIR);
